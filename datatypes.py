# Step 1: Declare and Initialize Variables
integer_var = 10
float_var = 3.14
string_var = "Hello"
boolean_var = True

# Step 2: Perform Operations
# Adding integer and float
sum_result = integer_var + float_var

# Concatenating string
concatenated_string = string_var + " World!"

# Negating boolean
negated_boolean = not boolean_var

# Step 3: Print Results
print("Integer Value:", integer_var)
print("Float Value:", float_var)
print("String Value:", string_var)
print("Boolean Value:", boolean_var)

print("Sum of Integer and Float:", sum_result)
print("Concatenated String:", concatenated_string)
print("Negated Boolean Value:", negated_boolean)
