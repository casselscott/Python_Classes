# Step 1: Create variables
a = 8
b = 3

# Step 2: Arithmetic operations
print(f"Addition: {a} + {b} = {a + b}")
print(f"Subtraction: {a} - {b} = {a - b}")
print(f"Multiplication: {a} * {b} = {a * b}")
print(f"Division: {a} / {b} = {a / b}")
print(f"Floor Division: {a} // {b} = {a // b}")
print(f"Modulus: {a} % {b} = {a % b}")
print(f"Exponentiation: {a} ** {b} = {a ** b}")

# Step 3: Comparison operators
print(f"Equal to: {a} == {b} = {a == b}")
print(f"Not equal to: {a} != {b} = {a != b}")
print(f"Greater than: {a} > {b} = {a > b}")
print(f"Less than: {a} < {b} = {a < b}")
print(f"Greater than or equal to: {a} >= {b} = {a >= b}")
print(f"Less than or equal to: {a} <= {b} = {a <= b}")

# Step 4: Logical operators
print(f"Logical AND: {a} > 5 and {b} < 15 = {a > 5 and b < 15}")
print(f"Logical OR: {a} < 10 or {b} > 20 = {a < 10 or b > 20}")
print(f"Logical NOT: not ({a} == {b}) = {not (a == b)}")

# Step 5: Assignment operators
a += 5
print(f"a += 5: {a}")
a -= 2
print(f"a -= 2: {a}")
a *= 3
print(f"a *= 3: {a}")
a /= 2
print(f"a /= 2: {a}")
a %= 4
print(f"a %= 4: {a}")
a //= 2
print(f"a //= 2: {a}")
a **= 2
print(f"a **= 2: {a}")
